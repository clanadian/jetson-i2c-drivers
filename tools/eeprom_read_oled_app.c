/* 문자열을 글자당 6바이트(폰트 5 + 여백 1) 비트맵으로 바꿔 row번째 줄에 출력 */
static int oled_print(int fd, int row, const char *s)
{
    uint8_t buf[OLED_COLS * 6];
    int len = 0;

    for (; *s && len < OLED_COLS; s++, len++) {
        int c = (*s < 0x20 || *s > 0x7E) ? 0 : *s - 0x20;
        memcpy(&buf[len * 6], font5x7[c], 5);
        buf[len * 6 + 5] = 0x00;
    }

    if (lseek(fd, row * OLED_COLS, SEEK_SET) < 0)
        return -1;

    if (write(fd, buf, len * 6) != len * 6)
        return -1;

    return 0;
}

int main(){
    struct at24c256_calib_data calib;

    int eep_fd = open("/dev/at24c256_i2c", O_RDONLY);
    if (eep_fd < 0) {
        perror("at24c256 open fail");
        return 1;
    }

    ssize_t n = read(eep_fd, &calib, sizeof(calib));
    close(eep_fd);
    if (n != (ssize_t)sizeof(calib)) {
        fprintf(stderr, "eeprom read fail (%zd)\n", n);
        return 1;
    }

    printf("accel offset: %d %d %d\n",
           calib.accel_offset_x, calib.accel_offset_y, calib.accel_offset_z);
    printf("gyro  offset: %d %d %d\n",
           calib.gyro_offset_x, calib.gyro_offset_y, calib.gyro_offset_z);

    int oled_fd = open("/dev/ssd1306_i2c", O_WRONLY);
    if (oled_fd < 0) {
        perror("ssd1306 open fail");
        return 1;
    }

    if (ioctl(oled_fd, SSD1306_IOC_CLEAR) < 0) {
        perror("ssd1306 clear fail");
        close(oled_fd);
        return 1;
    }

    char line[OLED_COLS + 1];
    const char *label[6] = { "AX", "AY", "AZ", "GX", "GY", "GZ" };
    int16_t vals[6] = {
        calib.accel_offset_x, calib.accel_offset_y, calib.accel_offset_z,
        calib.gyro_offset_x,  calib.gyro_offset_y,  calib.gyro_offset_z,
    };

    if (oled_print(oled_fd, 0, "CALIB OFFSET") < 0)
        goto oled_err;

    for (int i = 0; i < 6; i++) {
        snprintf(line, sizeof(line), "%s:%7d", label[i], vals[i]);
        if (oled_print(oled_fd, i + 1, line) < 0)
            goto oled_err;
    }

    close(oled_fd);
    return 0;

oled_err:
    perror("oled write fail");
    close(oled_fd);
    return 1;
}
